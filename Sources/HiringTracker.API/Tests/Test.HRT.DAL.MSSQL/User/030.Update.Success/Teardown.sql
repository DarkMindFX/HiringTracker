DECLARE @ID BIGINT
DECLARE @Login NVARCHAR(50) = 'Login 6381ee51cbec42d1bd78e44742b39605'
DECLARE @FirstName NVARCHAR(50) = 'FirstName 6381ee51cbec42d1bd78e44742b39605'
DECLARE @LastName NVARCHAR(50) = 'LastName 6381ee51cbec42d1bd78e44742b39605'
DECLARE @Email NVARCHAR(50) = 'Email 6381ee51cbec42d1bd78e44742b39605'
DECLARE @Description NVARCHAR(255) = 'Description 6381ee51cbec42d1bd78e44742b39605'
DECLARE @PwdHash NVARCHAR(255) = 'PwdHash 6381ee51cbec42d1bd78e44742b39605'
DECLARE @Salt NVARCHAR(255) = 'Salt 6381ee51cbec42d1bd78e44742b39605'
DECLARE @Fail AS BIT = 0

IF(NOT EXISTS(SELECT 1 FROM 
					[dbo].[User]
				WHERE 
					(CASE WHEN @Login IS NOT NULL THEN (CASE WHEN [Login] = @Login THEN 1 ELSE 0 END) ELSE 1 END) = 1 AND 
	(CASE WHEN @FirstName IS NOT NULL THEN (CASE WHEN [FirstName] = @FirstName THEN 1 ELSE 0 END) ELSE 1 END) = 1 AND 
