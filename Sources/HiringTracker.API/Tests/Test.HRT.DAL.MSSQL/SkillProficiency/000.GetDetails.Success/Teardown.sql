

DECLARE @ID BIGINT = 789372
DECLARE @Name NVARCHAR(50) = 'Name 90509afc721a4571bd22f97043cf2959'
 

DELETE FROM [SkillProficiency]
FROM 
	[dbo].[SkillProficiency] e
WHERE
	(CASE WHEN @ID IS NOT NULL THEN (CASE WHEN [ID] = @ID THEN 1 ELSE 0 END) ELSE 1 END) = 1 AND
	(CASE WHEN @Name IS NOT NULL THEN (CASE WHEN [Name] = @Name THEN 1 ELSE 0 END) ELSE 1 END) = 1 
